package vocabulary.model;

public class Voices {
	private String usAccentVoice;
	private String engAccentVoice;
	private String ausAccentVoices;
	
	public String getUsAccentVoice() {
		return usAccentVoice;
	}
	public void setUsAccentVoice(String usAccentVoice) {
		this.usAccentVoice = usAccentVoice;
	}
	public String getEngAccentVoice() {
		return engAccentVoice;
	}
	public void setEngAccentVoice(String engAccentVoice) {
		this.engAccentVoice = engAccentVoice;
	}
	public String getAusAccentVoices() {
		return ausAccentVoices;
	}
	public void setAusAccentVoices(String ausAccentVoices) {
		this.ausAccentVoices = ausAccentVoices;
	}
	
	
	
}
